const data = require('./data');

const { animals } = data;
// console.log(animals[0])

function animalsByIds(...ids) {
  const animalsId = [];
  if (ids.length === 0) {
    return [];
  }
  ids.forEach((id) => {
    const res = animals.filter((animal) => animal.id === id);
    animalsId.push(res[0]);
  });
  return animalsId;
}

